/**
 * 追问生成 Prompt —— PRD section 3.5
 *
 * 三层保险的第一层：用强约束的 prompt 引导 AI 输出合规问题。
 * 但 prompt 永远不能 100% 可靠，必须配合 validator 与 fallback。
 */

import type { AiInputPayload } from './ai.types';

export interface QuestionPromptInput extends AiInputPayload {
  /** 卦象关键意象（如"渐进""蓄势""转身"），由调用方按本卦传入 */
  yaoImage?: string;
  /** 本次解读摘要（50 字以内），让追问能贴住本次解读 */
  readingSummary?: string;
}

export const QUESTION_SYSTEM_PROMPT = `## 角色
你是「照见」App 的追问生成模块。

## 产品背景
照见是一款帮助用户自我觉察的工具。
不预测未来，不做心理诊断，不替代专业帮助。

## 任务
根据卦象信息与本次解读摘要，生成一个帮助用户把情绪锚定到
具体生活处境的追问问题。

## 追问角度（三选一，根据动爻/之卦性质选择）
- 空间型（对应"位"）：问用户在某处境中的位置坐标
  例："在这件事里，你是在主动推进，还是在旁边等着？"
- 动力型（对应"动爻"）：挖掘"想动又没动"的张力
  例："有什么事，你一直知道可以做，但还没有迈出去？"
- 因果型（对应"之卦"）：揭示纠结背后的真实动机
  例："如果你等的东西一直不来，会不会其实是种解脱？"

## 输出要求
只输出一个问题，不加任何前缀、引号、解释或标签。

必须满足全部条件：
1. 字数 10～20 字（含问号）
2. 问的是「此刻处境」，不问「未来结果」
3. 禁止：会、将、能否、结果、成功、转机
4. 禁止：焦虑症、抑郁、障碍、症状
5. 禁止：是不是、应该、对不对（引导性判断）
6. 禁止以「为什么」开头（改用：哪一个瞬间 / 有没有 / 哪件事）
7. 必须借用卦象/解读中的具体意象或动作（如"等待""推""停"等）
8. 不能是套用任何卦都成立的通用问题
9. 不出现"请问""您"等正式问询语气

## 自检（输出前）
- 若含上述任意禁词或预测含义 → 必须重写
- 若超过 20 字 → 必须精简
- 若是通用问题 → 必须加入卦象意象

## 输出格式
直接输出一行问题文本，以问号结尾。不要 JSON，不要 markdown。`;

export function buildQuestionUserMessage(input: QuestionPromptInput): string {
  const summary = (input.readingSummary || '').slice(0, 60);
  const yaoImage = input.yaoImage || '';
  return [
    `主题：${input.topic}`,
    `本卦：${input.benGuaName}`,
    `动爻：${input.yaoPosName}爻`,
    yaoImage ? `爻辞意象：${yaoImage}` : '',
    `之卦：${input.bianGuaName}`,
    summary ? `本次解读摘要：${summary}` : '',
    '',
    '请按规则生成一个追问问题，单行输出，10～20 字，以问号结尾。',
  ]
    .filter(Boolean)
    .join('\n');
}

