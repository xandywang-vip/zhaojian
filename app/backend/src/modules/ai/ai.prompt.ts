export function buildUserMessage(input: AiInputPayload): string {
  const yaoCiLine = input.dongYaoCi
    ? `动爻爻辞：${input.dongYaoCi}`
    : `动爻：${input.yaoPosName}爻`;

  return `主题：${input.topic}
本卦：${input.benGuaName}
${yaoCiLine}
之卦：${input.bianGuaName}

请按系统指示，将以上卦象意象融入四段心理疗愈文案，输出 JSON。`;
}

