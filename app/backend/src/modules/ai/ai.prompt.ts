// Extract canonical name from full descriptive name.
// "乾为天" → "乾"; "水雷屯" → "屯"; "风天小畜" → "小畜"; "天火同人" → "同人".
export function canonicalGuaName(fullName: string): string {
  if (!fullName) return '';
  if (fullName.length === 3 && fullName.charAt(1) === '为') {
    return fullName.charAt(0);
  }
  return fullName.length > 2 ? fullName.slice(2) : fullName;
}

const POS_NAMES = ['初', '二', '三', '四', '五', '上'];
export function yaoPosName(n: number): string {
  return POS_NAMES[n - 1] ?? String(n);
}

